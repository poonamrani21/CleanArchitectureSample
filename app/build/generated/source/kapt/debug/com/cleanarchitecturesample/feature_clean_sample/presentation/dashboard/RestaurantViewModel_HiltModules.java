package com.cleanarchitecturesample.feature_clean_sample.presentation.dashboard;

import androidx.lifecycle.ViewModel;
import dagger.Binds;
import dagger.Module;
import dagger.Provides;
import dagger.hilt.InstallIn;
import dagger.hilt.android.components.ActivityRetainedComponent;
import dagger.hilt.android.components.ViewModelComponent;
import dagger.hilt.android.internal.lifecycle.HiltViewModelMap;
import dagger.hilt.codegen.OriginatingElement;
import dagger.multibindings.IntoMap;
import dagger.multibindings.IntoSet;
import dagger.multibindings.StringKey;
import java.lang.String;

@OriginatingElement(
    topLevelClass = RestaurantViewModel.class
)
public final class RestaurantViewModel_HiltModules {
  private RestaurantViewModel_HiltModules() {
  }

  @Module
  @InstallIn(ViewModelComponent.class)
  public abstract static class BindsModule {
    private BindsModule() {
    }

    @Binds
    @IntoMap
    @StringKey("com.cleanarchitecturesample.feature_clean_sample.presentation.dashboard.RestaurantViewModel")
    @HiltViewModelMap
    public abstract ViewModel binds(RestaurantViewModel vm);
  }

  @Module
  @InstallIn(ActivityRetainedComponent.class)
  public static final class KeyModule {
    private KeyModule() {
    }

    @Provides
    @IntoSet
    @HiltViewModelMap.KeySet
    public static String provide() {
      return "com.cleanarchitecturesample.feature_clean_sample.presentation.dashboard.RestaurantViewModel";
    }
  }
}
